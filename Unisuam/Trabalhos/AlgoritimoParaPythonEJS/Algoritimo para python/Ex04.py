#⦁	Crie um programa que leia dois números e exiba o somatório, a subtração, a divisão e a multiplicação entre eles.

Numero01 = int(input("Digite o primeiro numero: "))
Numero02 = int(input("Digite o segundo numero: "))
print("A SOMA entre", Numero01, "e", Numero02, "é ", Numero01 + Numero02)
print("A SUBTRAÇÃO entre", Numero01, "e", Numero02, "é ", Numero01 - Numero02)
print("A MULTIPLICAÇÃO entre", Numero01, "e", Numero02, "é ", Numero01 * Numero02)
print("A DIVISÃO entre", Numero01, "e", Numero02, "é ", Numero01 / Numero02)